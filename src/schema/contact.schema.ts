import { Schema } from "mongoose";
import { Contact } from "../interface/contact.interface";
export const contactSchema = new Schema<Contact>(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    message: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    timestamps: true,
  }
);